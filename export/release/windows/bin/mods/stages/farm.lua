function onCreate()
    makeLuaSprite('blueScreen', nil, 0, 0, 0)
    makeGraphic('blueScreen', screenWidth, screenHeight, '000064')
    setObjectCamera('blueScreen', 'other')
    setProperty('blueScreen.alpha', 0.25)
    addLuaSprite('blueScreen')
	makeLuaSprite('sky', 'backgrounds/farm/sky', -600, -200)
	setLuaSpriteScrollFactor('sky', 0.25, 0.25)
	makeLuaSprite('flatgrass', 'backgrounds/farm/flatgrass', 350, 75)
	scaleObject('flatgrass', 0.5, 0.5)
	setLuaSpriteScrollFactor('flatgrass', 0.5, 0.5)
	makeLuaSprite('hills', 'backgrounds/farm/hills', -173, 125)
	setLuaSpriteScrollFactor('hills', 0.6, 0.6)
	makeLuaSprite('farmHouse', 'backgrounds/farm/farmHouse', 100, 125)
	setLuaSpriteScrollFactor('farmHouse', 0.7, 0.7)
	makeLuaSprite('ground', 'backgrounds/farm/ground', -600, 500)
	makeLuaSprite('cornFence0', 'backgrounds/farm/cornFence0', -400, 200)
	makeLuaSprite('cornFence1', 'backgrounds/farm/cornFence1', 1100, 200)
	makeLuaSprite('cornBag', 'backgrounds/farm/cornBag', 1200, 550)
	makeLuaSprite('sign', 'backgrounds/farm/sign', 0, 350)
	addLuaSprite('sky', false)
	addLuaSprite('flatgrass', false)
	addLuaSprite('hills', false)
	addLuaSprite('farmHouse', false)
	addLuaSprite('ground', false)
	addLuaSprite('cornFence0', false)
	addLuaSprite('cornFence1', false)
	addLuaSprite('cornBag', false)
	addLuaSprite('sign', false)
end