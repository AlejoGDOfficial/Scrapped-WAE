function onCreate()
    makeLuaSprite('orangeScreen', nil, 0, 0, 0)
    makeGraphic('orangeScreen', screenWidth, screenHeight, 'FF8000')
    setObjectCamera('orangeScreen', 'other')
    setProperty('orangeScreen.alpha', 0.15)
    addLuaSprite('orangeScreen')
	makeLuaSprite('sky', 'backgrounds/backyardAfternoon/sky', -600, -300)
	setLuaSpriteScrollFactor('sky', 0.25, 0.25)
	makeLuaSprite('mountains', 'backgrounds/backyardAfternoon/mountains', -600, -300)
	setLuaSpriteScrollFactor('mountains', 0.5, 0.5)
	makeLuaSprite('ground', 'backgrounds/backyardAfternoon/ground', -600, -300)
	setLuaSpriteScrollFactor('ground', 1, 1)
	addLuaSprite('sky', false)
	addLuaSprite('mountains', false)
	addLuaSprite('ground', false)
end