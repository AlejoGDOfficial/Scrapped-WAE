function onCreate()
	makeLuaSprite('sky', 'backgrounds/n1/sky', -600, -300)
	setLuaSpriteScrollFactor('sky', 0.25, 0.25)
	makeLuaSprite('mountains', 'backgrounds/n1/mountains', -600, -300)
	setLuaSpriteScrollFactor('mountains', 0.5, 0.5)
	makeLuaSprite('background', 'backgrounds/n1/background', -600, -300)
	setLuaSpriteScrollFactor('background', 0.75, 0.75)
	makeLuaSprite('ground', 'backgrounds/n1/ground', -600, -300)
	setLuaSpriteScrollFactor('ground', 1, 1)
	setProperty('ground.scale.x', 1.25)
	setProperty('ground.scale.y', 1.25)
	addLuaSprite('sky', false)
	addLuaSprite('mountains', false)
	addLuaSprite('background', false)
	addLuaSprite('ground', false)
end