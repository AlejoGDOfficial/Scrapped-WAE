function onCreate()
    makeLuaSprite('blueScreen', nil, 0, 0, 0)
    makeGraphic('blueScreen', screenWidth, screenHeight, '000064')
    setObjectCamera('blueScreen', 'other')
    setProperty('blueScreen.alpha', 0.125)
    addLuaSprite('blueScreen')
	makeLuaSprite('sky', 'backgrounds/venezuelaNight/sky', -600, -300)
	setLuaSpriteScrollFactor('sky', 0.25, 0.25)
	makeLuaSprite('background', 'backgrounds/venezuelaNight/background', -600, -300)
	setLuaSpriteScrollFactor('background', 0.5, 0.5)
	makeLuaSprite('ground', 'backgrounds/venezuelaNight/ground', -600, -300)
	setLuaSpriteScrollFactor('ground', 1, 1)
	addLuaSprite('sky', false)
	addLuaSprite('background', false)
	addLuaSprite('ground', false)
end