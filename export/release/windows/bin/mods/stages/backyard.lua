function onCreate()
    makeLuaSprite('yellowScreen', nil, 0, 0, 0)
    makeGraphic('yellowScreen', screenWidth, screenHeight, 'FFFF00')
    setObjectCamera('yellowScreen', 'other')
    setProperty('yellowScreen.alpha', 0.075)
    addLuaSprite('yellowScreen')
	makeLuaSprite('sky', 'backgrounds/backyard/sky', -600, -300)
	setLuaSpriteScrollFactor('sky', 0.25, 0.25)
	makeLuaSprite('mountains', 'backgrounds/backyard/mountains', -600, -300)
	setLuaSpriteScrollFactor('mountains', 0.5, 0.5)
	makeLuaSprite('ground', 'backgrounds/backyard/ground', -600, -300)
	setLuaSpriteScrollFactor('ground', 1, 1)
	addLuaSprite('sky', false)
	addLuaSprite('mountains', false)
	addLuaSprite('ground', false)
end